const express = require("express");
const mongoose = require("mongoose");
const Product = require("../models/Product");
const Category = require("../models/Category");
const User = require("../models/User");
const Profile = require("../models/Profile");
const Review = require("../models/Review");
const router = express.Router();
const verifyToken = require("../middleware/authMiddleware");
const roleCheck = require("../middleware/roleMiddleware");

// Middleware to check if user is approved seller
const isApprovedSeller = async (req, res, next) => {
    try {
        const user = await User.findById(req.user.id);

        if (user.role === "admin") {
            return next();
        }

        if (user.role !== "seller_approved") {
            return res.status(403).json({
                message: "Access denied. Please complete seller verification to add products."
            });
        }

        next();
    } catch (error) {
        res.status(500).json({ message: "Server error", error: error.message });
    }
};

// Add Product (only approved seller and admin)
router.post("/", verifyToken, isApprovedSeller, async (req, res) => {
    try {
        const { name, description, price, stock, category, images, tags, salePrice, featured } = req.body;
        const sellerId = req.user.id;

        const categoryExists = await Category.findById(category);
        if (!categoryExists) return res.status(400).json({ message: "Invalid category" });

        const product = new Product({
            name,
            description,
            price,
            stock,
            category,
            images: images || [],
            tags: tags || [],
            salePrice,
            featured: featured || false,
            seller: sellerId
        });

        await product.save();

        // Create automatic 1-star review
        const defaultReview = new Review({
            product: product._id,
            user: sellerId,
            rating: 1,
            comment: "New product - awaiting customer reviews",
            isAutoGenerated: true
        });

        await defaultReview.save();

        // Update product with review stats
        product.numReviews = 1;
        product.averageRating = 1;
        await product.save();

        const populatedProduct = await Product.findById(product._id)
            .populate("category", "name")
            .populate("seller", "name email");

        res.status(201).json({
            message: "Product created successfully",
            product: populatedProduct
        });
    } catch (err) {
        console.error("Product creation error:", err);
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

// Get All Products with filters
router.get("/", async (req, res) => {
    try {
        const { category, search, minPrice, maxPrice, status, featured } = req.query;

        let filter = {};

        if (category && category !== 'all') filter.category = category;
        if (status && status !== 'all') filter.status = status;
        if (featured && featured !== 'all') filter.featured = featured === 'true';

        if (search) {
            filter.$or = [
                { name: { $regex: search, $options: 'i' } },
                { description: { $regex: search, $options: 'i' } },
                { tags: { $in: [new RegExp(search, 'i')] } }
            ];
        }

        if (minPrice || maxPrice) {
            filter.price = {};
            if (minPrice) filter.price.$gte = parseFloat(minPrice);
            if (maxPrice) filter.price.$lte = parseFloat(maxPrice);
        }

        const products = await Product.find(filter)
            .populate("category", "name")
            .populate("seller", "name email")
            .sort({ createdAt: -1 });

        res.json({
            products,
            total: products.length
        });
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

