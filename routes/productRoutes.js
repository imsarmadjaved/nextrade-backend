// Middleware to check if user is approved seller
const isApprovedSeller = async (req, res, next) => {
    try {
        const user = await User.findById(req.user.id);
        if (user.role === "admin") return next();
        if (user.role !== "seller_approved") {
            return res.status(403).json({
                message: "Access denied. Complete seller verification to add products."
            });
        }
        next();
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
};

router.post("/", verifyToken, isApprovedSeller, async (req, res) => {
    try {
        const { name, description, price, stock, category, tags, salePrice, featured, images } = req.body;
        const sellerId = req.user.id;

        console.log("=== PRODUCT CREATION DEBUG ===");
        console.log("User ID:", sellerId);
        console.log("Images received:", images);
        console.log("Type of images:", typeof images);
        console.log("Is array:", Array.isArray(images));
        console.log("=== END DEBUG ===");

        // Validate required fields
        if (!name || !description || !price || !stock || !category) {
            return res.status(400).json({ message: "Missing required fields" });
        }

        // Validate images
        if (!images || !Array.isArray(images) || images.length === 0) {
            return res.status(400).json({
                message: "No images provided",
                imagesReceived: images
            });
        }

        const categoryExists = await Category.findById(category);
        if (!categoryExists) return res.status(400).json({ message: "Invalid category" });

        const tagsArray = tags
            ? Array.isArray(tags)
                ? tags
                : tags.split(",").map(t => t.trim())
            : [];

        // Create product with images from request body
        const product = new Product({
            name,
            description,
            price,
            stock,
            category,
            images: images,
            tags: tagsArray,
            salePrice,
            featured: featured || false,
            seller: sellerId,
        });

        console.log("Product to save:", product);
        await product.save();

        // Create default review
        const defaultReview = new Review({
            product: product._id,
            user: sellerId,
            rating: 1,
            comment: "New product - awaiting customer reviews",
            isAutoGenerated: true,
        });
        await defaultReview.save();

        product.numReviews = 1;
        product.averageRating = 1;
        await product.save();

        res.status(201).json({
            message: "Product created successfully",
            product
        });
    } catch (err) {
        console.error("PRODUCT CREATION ERROR:", err);
        console.error("Error stack:", err.stack);
        res.status(500).json({
            message: "Server error",
            error: err.message,
            stack: process.env.NODE_ENV === 'development' ? err.stack : undefined
        });
    }
});

// Get all products with filters
router.get("/", async (req, res) => {
    try {
        const { category, search, minPrice, maxPrice, status, featured } = req.query;
        let filter = {};

        if (category && category !== 'all') filter.category = category;
        if (status && status !== 'all') filter.status = status;
        if (featured && featured !== 'all') filter.featured = featured === 'true';

        if (search) {
            filter.$or = [
                { name: { $regex: search, $options: 'i' } },
                { description: { $regex: search, $options: 'i' } },
                { tags: { $in: [new RegExp(search, 'i')] } }
            ];
        }

        if (minPrice || maxPrice) {
            filter.price = {};
            if (minPrice) filter.price.$gte = parseFloat(minPrice);
            if (maxPrice) filter.price.$lte = parseFloat(maxPrice);
        }

        const products = await Product.find(filter)
            .populate("category", "name")
            .populate("seller", "name email")
            .sort({ createdAt: -1 });

        res.json({ products, total: products.length });
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

