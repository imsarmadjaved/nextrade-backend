// Update product with optional image upload
router.put("/:id", verifyToken, roleCheck(["seller", "admin"]), async (req, res) => {
    try {
        const { name, description, price, stock, category, tags, salePrice, featured, status, images } = req.body;

        console.log("=== PRODUCT UPDATE DEBUG ===");
        console.log("Raw images received:", images);
        console.log("=== END DEBUG ===");

        // Validate required fields
        if (!name || !description || !price || !stock || !category) {
            return res.status(400).json({ message: "Missing required fields" });
        }

        const categoryExists = await Category.findById(category);
        if (!categoryExists) return res.status(400).json({ message: "Invalid category" });

        // Process tags
        const tagsArray = tags
            ? Array.isArray(tags)
                ? tags
                : tags.split(",").map(t => t.trim())
            : [];

        // Clean images - remove _id field and handle mixed formats
        let cleanImages = [];
        if (images && Array.isArray(images)) {
            cleanImages = images.map(img => {
                // If it's an object with url
                if (img && typeof img === 'object' && img.url) {
                    return {
                        url: img.url,
                        publicId: img.publicId || null
                    };
                }
                // If it's a string
                if (typeof img === 'string') {
                    return {
                        url: img,
                        publicId: null
                    };
                }
                return null;
            }).filter(img => img !== null); // Remove null entries
        }

        console.log("Cleaned images:", cleanImages);

        // Create update data
        const updateData = {
            name,
            description,
            price: parseFloat(price),
            stock: parseInt(stock),
            category,
            images: cleanImages, // Use cleaned images
            tags: tagsArray,
            featured: featured || false,
            status: status || "active",
        };

        // Handle salePrice
        if (salePrice !== undefined && salePrice !== null && salePrice !== "") {
            updateData.salePrice = parseFloat(salePrice);
        }

        const product = await Product.findByIdAndUpdate(
            req.params.id,
            updateData,
            { new: true }
        );

        if (!product) return res.status(404).json({ message: "Product not found" });

        res.json({
            message: "Product updated successfully",
            product
        });
    } catch (err) {
        console.error("PRODUCT UPDATE ERROR:", err);
        console.error("Error details:", err.message);
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

router.delete("/:id", verifyToken, roleCheck(["seller", "admin"]), async (req, res) => {
    try {
        const productId = req.params.id;
        const userId = req.user.id;
        const user = await User.findById(userId);

        console.log("=== PRODUCT DELETE REQUEST ===");
        console.log("Product ID:", productId);
        console.log("User ID:", userId);
        console.log("User role:", user.role);
        console.log("=== END DEBUG ===");

        const product = await Product.findById(productId);

        if (!product) {
            return res.status(404).json({ message: "Product not found" });
        }

        // Check authorization: admin can delete any product, seller can only delete their own
        if (user.role !== "admin" && product.seller.toString() !== userId) {
            return res.status(403).json({
                message: "Not authorized to delete this product",
                productOwner: product.seller.toString(),
                currentUser: userId
            });
        }

        await Product.findByIdAndDelete(productId);

        res.json({
            message: "Product deleted successfully",
            productId: productId
        });
    } catch (err) {
        console.error("PRODUCT DELETE ERROR:", err);
        res.status(500).json({
            message: "Server error",
            error: err.message
        });
    }
});

