// Get products
router.get("/admin/products", verifyToken, roleCheck(["admin"]), async (req, res) => {
    try {
        const {
            page = 1,
            limit = 10,
            category,
            search,
            minPrice,
            maxPrice,
            status,
            featured,
            sortBy = 'createdAt',
            sortOrder = 'desc'
        } = req.query;

        let filter = {};
        if (category && category !== 'all') filter.category = category;
        if (status && status !== 'all') filter.status = status;
        if (featured && featured !== 'all') filter.featured = featured === 'true';

        if (search) {
            filter.$or = [
                { name: { $regex: search, $options: 'i' } },
                { description: { $regex: search, $options: 'i' } },
                { tags: { $in: [new RegExp(search, 'i')] } }
            ];
        }

        if (minPrice || maxPrice) {
            filter.price = {};
            if (minPrice) filter.price.$gte = parseFloat(minPrice);
            if (maxPrice) filter.price.$lte = parseFloat(maxPrice);
        }

        const sort = {};
        sort[sortBy] = sortOrder === 'asc' ? 1 : -1;

        const products = await Product.find(filter)
            .populate("category", "name")
            .populate("seller", "name email")
            .sort(sort)
            .limit(limit * 1)
            .skip((page - 1) * limit);

        const total = await Product.countDocuments(filter);

        res.json({
            products,
            totalPages: Math.ceil(total / limit),
            currentPage: page,
            total
        });
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

// Bulk product actions (Admin only)
router.post("/admin/products/bulk-actions", verifyToken, roleCheck(["admin"]), async (req, res) => {
    try {
        const { productIds, action, data } = req.body;

        if (!productIds || !Array.isArray(productIds) || productIds.length === 0) {
            return res.status(400).json({ message: "Product IDs are required" });
        }

        let update = {};
        switch (action) {
            case 'activate':
                update = { status: 'active' };
                break;
            case 'deactivate':
                update = { status: 'inactive' };
                break;
            case 'feature':
                update = { featured: true };
                break;
            case 'unfeature':
                update = { featured: false };
                break;
            case 'update-stock':
                if (!data || !data.stock) {
                    return res.status(400).json({ message: "Stock value is required" });
                }
                update = { stock: parseInt(data.stock) };
                break;
            case 'delete':
                await Product.deleteMany({ _id: { $in: productIds } });
                return res.json({ message: `${productIds.length} products deleted` });
            default:
                return res.status(400).json({ message: "Invalid action" });
        }

        const result = await Product.updateMany(
            { _id: { $in: productIds } },
            update
        );

        res.json({
            message: `${result.modifiedCount} products updated`,
            modifiedCount: result.modifiedCount
        });
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

router.get("/seller/:sellerId/count", async (req, res) => {
    try {
        const { sellerId } = req.params;

        // Validate sellerId
        if (!mongoose.Types.ObjectId.isValid(sellerId)) {
            return res.status(400).json({ message: "Invalid seller ID" });
        }

        // Check if seller exists and is approved
        const seller = await User.findById(sellerId);
        if (!seller || seller.role !== "seller_approved") {
            return res.status(404).json({ message: "Seller not found or not approved" });
        }

        const productCount = await Product.countDocuments({
            seller: sellerId,
            status: 'active' // Only count active products
        });

        res.json({
            productCount,
            sellerName: seller.name,
            storeName: seller.storeName
        });
    } catch (error) {
        console.error("Product count error:", error);
        res.status(500).json({ message: "Server error", error: error.message });
    }
});

