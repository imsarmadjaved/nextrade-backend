// Get seller's public profile with product count
router.get("/seller/:sellerId/public-profile", async (req, res) => {
    try {
        const { sellerId } = req.params;

        // Validate sellerId
        if (!mongoose.Types.ObjectId.isValid(sellerId)) {
            return res.status(400).json({ message: "Invalid seller ID" });
        }

        // Check if seller exists and is approved
        const seller = await User.findById(sellerId).select("name email storeName storeDescription role");
        if (!seller || seller.role !== "seller_approved") {
            return res.status(404).json({ message: "Seller not found or not approved" });
        }

        // Get seller profile
        const profile = await Profile.findOne({ user: sellerId }).select(
            "businessType city yearsInBusiness mainProducts businessDescription profileImage"
        );

        // Get product count
        const productCount = await Product.countDocuments({
            seller: sellerId,
            status: 'active'
        });

        // Get active products count
        const activeProducts = await Product.countDocuments({
            seller: sellerId,
            status: 'active',
            stock: { $gt: 0 }
        });

        res.json({
            seller: {
                id: seller._id,
                name: seller.name,
                storeName: seller.storeName,
                storeDescription: seller.storeDescription,
                role: seller.role
            },
            profile: {
                businessType: profile?.businessType,
                city: profile?.city,
                yearsInBusiness: profile?.yearsInBusiness || 0,
                mainProducts: profile?.mainProducts || [],
                businessDescription: profile?.businessDescription,
                profileImage: profile?.profileImage
            },
            stats: {
                totalProducts: productCount,
                activeProducts: activeProducts
            }
        });
    } catch (error) {
        console.error("Seller public profile error:", error);
        res.status(500).json({ message: "Server error", error: error.message });
    }
});

// Update Product (only seller and admin)
router.put("/:id", verifyToken, roleCheck(["seller", "admin"]), async (req, res) => {
    try {
        const { name, description, price, stock, category, images, tags, salePrice, featured, status } = req.body;

        if (category) {
            const categoryExists = await Category.findById(category);
            if (!categoryExists) return res.status(400).json({ message: "Invalid category" });
        }

        const product = await Product.findByIdAndUpdate(
            req.params.id,
            {
                name,
                description,
                price,
                stock,
                category,
                images,
                tags,
                salePrice,
                featured,
                status
            },
            { new: true }
        )
            .populate("category", "name")
            .populate("seller", "name email");

        if (!product) return res.status(404).json({ message: "Product not found" });

        res.json({
            message: "Product updated successfully",
            product
        });
    } catch (err) {
        res.status(500).json({ message: "Server error", error: err.message });
    }
});

